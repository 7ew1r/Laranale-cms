    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::findOrFail($id);

        // Also delete the posts of the user
        foreach ($user->posts as $post) {
            $post->delete();
        }

        $user->delete();
